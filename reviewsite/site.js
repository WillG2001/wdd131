let reviewContainer = document.querySelector('#reviewSection');

let button = document.querySelector('.searchBtn');

button.addEventListener('click', search);

document.querySelector('#category').addEventListener('change', search);

function search() {
    let reviewQuery = document.querySelector('#search').value;
    let selectedCategory = document.querySelector('#category').value;

    let filterReviews = reviews.filter(function (review) {
        let matchesText =
            review.title.toLowerCase().includes(reviewQuery.toLowerCase()) ||
            review.reviewText.toLowerCase().includes(reviewQuery.toLowerCase());

        let matchesCategory =
            selectedCategory === 'all' || review.category === selectedCategory;

        return matchesText && matchesCategory;
    });

    let sortedReviews = filterReviews.sort(compareReviews);

    function compareReviews(a, b) {
        if (a.title < b.title) {
            return -1;
        } else if (a.title > b.title) {
            return 1;
        }
        return 0;
    }

    // clear out screen
    reviewContainer.innerHTML = ''

    // output on screen
    sortedReviews.forEach(function (review) {
        renderReview(review);
    })
}

function reviewTemplate(review) {
    return `
    <div class="review">
      <h2>${review.title}</h2>
      <img src="${review.image}" alt="${review.imageAlt}" class="reviewImg">
      <p>${review.reviewText}</p>
      <h3>Rating: ${review.rating}</h3>
    </div>
  `
}

function renderReview(review) {
    let html = reviewTemplate(review);
    reviewContainer.innerHTML += html;
}

function init() {
    reviews.forEach(r => { renderReview(r) });
}

init();